CREATE TABLE movie (
  movie_id integer PRIMARY KEY,
  movie_name text NOT NULL,
  poster text NOT NULL DEFAULT 'img/poster/no_poster.jpg',
  premiere_date date NOT NULL,
  age_restriction numeric(2, 0) NOT NULL DEFAULT 18,
  duration time NOT NULL,
  description text,
  country text,
  director text,
  genre text,
  youtube_trailer text
);

CREATE TABLE hall (
  hall_id serial PRIMARY KEY,
  hall_name text UNIQUE NOT NULL
);

CREATE TABLE showtime (
  showtime_id serial PRIMARY KEY,
  showtime_date timestamptz NOT NULL,
  price numeric(15, 4) NOT NULL,
  movie_id integer REFERENCES movie,
  hall_id integer REFERENCES hall
);

CREATE TABLE seat (
  seat_id serial PRIMARY KEY,
  seat_row integer,
  seat_number integer,
  hall_id integer REFERENCES hall
);

CREATE TABLE seat_status (
  seat_status_id serial PRIMARY KEY,
  seat_status_name varchar(32)
);

CREATE TABLE seat_status_log (
  seat_id integer REFERENCES seat,
  seat_status_id integer REFERENCES seat_status,
  seat_status_date timestamptz NOT NULL,
  PRIMARY KEY (seat_id, seat_status_date)
);

CREATE TABLE reservation (
  reservation_id serial PRIMARY KEY,
  phone text NOT NULL,
  showtime_id integer REFERENCES showtime
);

CREATE TABLE seat_to_reservation (
  reservation_id integer REFERENCES reservation,
  seat_id integer REFERENCES seat,
  PRIMARY KEY (reservation_id, seat_id)
);

